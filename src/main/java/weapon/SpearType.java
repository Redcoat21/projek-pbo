package weapon;

public enum SpearType {
    /**
     * Weakest spear type
     */
    IRON_SPEAR,
    /**
     * The only spear type to attack in crescent motion, similar to Sword
     */
    GLAIVE,
    /**
     * A Gigantic spear, attack in thrusting motion with a huge damage.
     */
    SPEAR_OF_THE_LORD,

}
